#!/usr/bin/env bash
# Regression tests for scripts/local-ci.sh node_modules freshness detection.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
LOCAL_CI="$REPO_ROOT/scripts/local-ci.sh"

source "$SCRIPT_DIR/lib/test_runner.sh"
source "$SCRIPT_DIR/lib/assertions.sh"

extract_function_block() {
    local function_name="$1"
    awk -v function_name="$function_name" '
        $0 ~ "^" function_name "\\(\\) \\{$" { in_block=1; print; next }
        in_block { print }
        in_block && /^}/ { exit }
    ' "$LOCAL_CI"
}

load_node_modules_guard() {
    eval "$(extract_function_block node_modules_fresh_or_fail)"
}

write_fixture_lockfile() {
    local web_dir="$1"
    printf '{"lockfileVersion": 3}\n' > "$web_dir/package-lock.json"
}

build_fixture() {
    local marker_name="$1"
    local tmpdir
    tmpdir="$(mktemp -d)"
    mkdir -p "$tmpdir/web/node_modules"
    write_fixture_lockfile "$tmpdir/web"
    printf 'installed\n' > "$tmpdir/web/node_modules/$marker_name"
    echo "$tmpdir"
}

run_guard_for_fixture() {
    local fixture_dir="$1"
    local output status
    REPO_ROOT="$fixture_dir"
    status=0
    output="$(node_modules_fresh_or_fail 2>&1)" || status=$?
    GUARD_STATUS="$status"
    GUARD_OUTPUT="$output"
}

test_accepts_npm_install_marker() {
    local dir
    dir="$(build_fixture ".package-lock.json")"
    run_guard_for_fixture "$dir"
    rm -rf "$dir"

    assert_eq "$GUARD_STATUS" "0" "node_modules guard accepts npm install marker"
}

test_rejects_pnpm_only_install_marker() {
    local dir
    dir="$(build_fixture ".modules.yaml")"
    run_guard_for_fixture "$dir"
    rm -rf "$dir"

    assert_eq "$GUARD_STATUS" "1" "node_modules guard rejects pnpm-only install marker (CI runs npm ci)"
    assert_contains "$GUARD_OUTPUT" "no npm install marker" "pnpm-only rejection names the missing npm marker"
}

test_rejects_missing_install_marker() {
    local dir
    dir="$(mktemp -d)"
    mkdir -p "$dir/web/node_modules"
    write_fixture_lockfile "$dir/web"
    run_guard_for_fixture "$dir"
    rm -rf "$dir"

    assert_eq "$GUARD_STATUS" "1" "node_modules guard rejects installs without an npm marker"
    assert_contains "$GUARD_OUTPUT" "no npm install marker" "missing marker error names the npm marker and remedy"
}

test_rejects_stale_npm_install_marker() {
    local dir
    dir="$(build_fixture ".package-lock.json")"
    touch -t 202401010000 "$dir/web/node_modules/.package-lock.json"
    touch -t 202501010000 "$dir/web/package-lock.json"
    run_guard_for_fixture "$dir"
    rm -rf "$dir"

    assert_eq "$GUARD_STATUS" "1" "node_modules guard rejects stale npm install marker"
    assert_contains "$GUARD_OUTPUT" "package-lock.json is newer" "stale marker error names lockfile freshness"
}

echo "=== local-ci node_modules guard tests ==="
load_node_modules_guard
test_accepts_npm_install_marker
test_rejects_pnpm_only_install_marker
test_rejects_missing_install_marker
test_rejects_stale_npm_install_marker
run_test_summary
